    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        // TODO add your handling code here:
        String datosEstudiante = null;
        String [] vectorDatos = null;

        tablaModelo = new DefaultTableModel();

        tablaModelo.addColumn("Documento");
        tablaModelo.addColumn("Nombres");
        tablaModelo.addColumn("Apellidos");
        tablaModelo.addColumn("Carrera");
        tablaModelo.addColumn("Sede");
        tablaModelo.addColumn("Semestre");

        this.jTableEstudiante.setModel(tablaModelo);

        try{
            FileReader FR = new FileReader("C:\\Users\\ASPIRE  -56AW\\Documents\\NetBeansProjects1\\HerenciaPersona\\Estudiante.txt"); //Excepcion al abrir el archivo FileNotFoundException
            BufferedReader BR = new BufferedReader(FR);
            datosEstudiante = BR.readLine();  //Excepcion al leer el archivo IOException

            while(datosEstudiante != null){
                vectorDatos = datosEstudiante.split(";"); //Separamos por campos
                tablaModelo.addRow(vectorDatos); //Cargamos a la tabla
                datosEstudiante = BR.readLine();
            }
            BR.close();
            JOptionPane.showMessageDialog(this, "Datos Estudiantes Cargados Correctamente");

        } catch(FileNotFoundException e){ //Excepcion al abir el archivo
            JOptionPane.showMessageDialog(this, "No fue posible abrir el archivo" + e);

        } catch(IOException ex){
            JOptionPane.showMessageDialog(null, "No fue posible abrir el archivo" + ex);

        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

